const express = require('express');
const http = require('http');
const { Server } = require('socket.io');
const cors = require('cors');
const axios = require('axios');

const app = express();
app.use(cors());
const server = http.createServer(app);

// –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è Socket.IO
const io = new Server(server, {
  cors: {
    origin: [
      "https://silexp.ru",
      "https://silexp-chat-server.onrender.com",
      "http://localhost:8000",
      "http://localhost:3000"
    ],
    methods: ["GET", "POST"],
    credentials: true
  },
  transports: ['websocket', 'polling'],
  allowEIO3: true
});

const DJANGO_URL = "https://silexp.ru";
const roomConnections = new Map();

// –¢–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è –∫ Django
console.log('üîç Testing Django connection...');

axios.get(`${DJANGO_URL}/api/test/`)
  .then(response => {
    console.log('‚úÖ Django connection successful:', response.data);
  })
  .catch(error => {
    console.error('‚ùå Django connection failed:', error.message);
  });

// –û–±—Ä–∞–±–æ—Ç–∫–∞ Socket.IO –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–π
io.on('connection', (socket) => {
  console.log('‚úÖ User connected:', socket.id);
  
  socket.on('ping', (cb) => {
    if (typeof cb === 'function') cb();
  });

  // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ disconnect
  socket.on('disconnect', (reason) => {
    console.log('‚ùå User disconnected:', socket.id, 'Reason:', reason);
    
    if (socket.roomName && roomConnections.has(socket.roomName) && socket.user_id) {
      // –£–¥–∞–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø–æ user_id
      if (roomConnections.get(socket.roomName).has(socket.user_id)) {
        const userInfo = roomConnections.get(socket.roomName).get(socket.user_id);
        roomConnections.get(socket.roomName).delete(socket.user_id);
        
        const onlineCount = roomConnections.get(socket.roomName).size;
        const users = Array.from(roomConnections.get(socket.roomName).values());
        
        io.to(socket.roomName).emit('online_users_update', { 
          count: onlineCount,
          room: socket.roomName,
          project_id: socket.project_id,
          users: users
        });
        
        console.log(`üë§ User ${userInfo.username} left room ${socket.roomName}, now ${onlineCount} users`);
        
        if (roomConnections.get(socket.roomName).size === 0) {
          roomConnections.delete(socket.roomName);
          console.log(`üóëÔ∏è Room ${socket.roomName} deleted (empty)`);
        }
      }
    }
  });

  // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ leave_project_chat
  socket.on('leave_project_chat', (roomData) => {
    try {
      const { project_id, user_id } = roomData;
      const roomName = `project_${project_id}`;
      
      console.log(`üë§ User requested to leave room: ${roomName}`);
      
      if (roomConnections.has(roomName) && user_id) {
        if (roomConnections.get(roomName).has(user_id)) {
          const userInfo = roomConnections.get(roomName).get(user_id);
          roomConnections.get(roomName).delete(user_id);
          
          const onlineCount = roomConnections.get(roomName).size;
          const users = Array.from(roomConnections.get(roomName).values());
          
          io.to(roomName).emit('online_users_update', { 
            count: onlineCount,
            room: roomName,
            project_id: project_id,
            users: users
          });
          
          console.log(`üë§ User ${userInfo.username} left room ${roomName}, now ${onlineCount} users`);
          
          if (onlineCount === 0) {
            roomConnections.delete(roomName);
            console.log(`üóëÔ∏è Room ${roomName} deleted (empty)`);
          }
        }
        
        socket.leave(roomName);
        console.log(`üö™ Socket left room: ${roomName}`);
      }
    } catch (error) {
      console.error('‚ùå Error leaving room:', error);
    }
  });

  // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ rejoin_project_chat
  socket.on('rejoin_project_chat', async (roomData) => {
    try {
      const { project_id, user_id, username } = roomData;
      const roomName = `project_${project_id}`;
      
      console.log(`üîÅ User rejoining room: ${roomName}`);
      
      // –í—ã—Ö–æ–¥–∏–º –∏–∑ –ø—Ä–µ–¥—ã–¥—É—â–µ–π –∫–æ–º–Ω–∞—Ç—ã –µ—Å–ª–∏ –±—ã–ª–∞
      if (socket.roomName && socket.roomName !== roomName) {
        if (roomConnections.has(socket.roomName) && roomConnections.get(socket.roomName).has(user_id)) {
          roomConnections.get(socket.roomName).delete(user_id);
          
          const onlineCount = roomConnections.get(socket.roomName).size;
          const users = Array.from(roomConnections.get(socket.roomName).values());
          
          io.to(socket.roomName).emit('online_users_update', { 
            count: onlineCount,
            room: socket.roomName,
            project_id: socket.roomName.replace('project_', ''),
            users: users
          });
          
          if (onlineCount === 0) {
            roomConnections.delete(socket.roomName);
          }
        }
        socket.leave(socket.roomName);
      }
      
      // –ü—Ä–∏—Å–æ–µ–¥–∏–Ω—è–µ–º —Å–æ–∫–µ—Ç –∫ –∫–æ–º–Ω–∞—Ç–µ
      socket.join(roomName);
      socket.roomName = roomName;
      socket.project_id = project_id;
      socket.user_id = user_id;
      
      // –°–æ–∑–¥–∞–µ–º –∫–æ–º–Ω–∞—Ç—É –µ—Å–ª–∏ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
      if (!roomConnections.has(roomName)) {
        roomConnections.set(roomName, new Map());
      }
      
      // –î–æ–±–∞–≤–ª—è–µ–º/–æ–±–Ω–æ–≤–ª—è–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –ø–æ user_id
      roomConnections.get(roomName).set(user_id, { 
        user_id, 
        username,
        socket_id: socket.id,
        joined_at: new Date().toISOString()
      });
      
      const onlineCount = roomConnections.get(roomName).size;
      const users = Array.from(roomConnections.get(roomName).values());
      
      // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –í–°–ï–ú –≤ –∫–æ–º–Ω–∞—Ç–µ
      io.to(roomName).emit('online_users_update', { 
        count: onlineCount,
        room: roomName,
        project_id: project_id,
        users: users
      });
      
      // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∏—Å—Ç–æ—Ä–∏—é —Å–æ–æ–±—â–µ–Ω–∏–π –≤–æ–∑–≤—Ä–∞—â–∞—é—â–µ–º—É—Å—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é
      try {
        const response = await axios.get(`${DJANGO_URL}/api/get-messages/${project_id}/`);
        socket.emit('message_history', response.data);
      } catch (error) {
        console.error('‚ùå Error fetching message history for rejoin:', error.message);
      }
      
      console.log(`üë§ User ${username} rejoined room ${roomName}, now ${onlineCount} users`);
    } catch (error) {
      console.error('‚ùå Error rejoining room:', error);
    }
  });

  // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∑–∞–ø—Ä–æ—Å–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –∫–æ–º–Ω–∞—Ç—ã
  socket.on('request_room_update', (roomData) => {
    try {
      const { project_id } = roomData;
      const roomName = `project_${project_id}`;
      
      if (roomConnections.has(roomName)) {
        const onlineCount = roomConnections.get(roomName).size;
        const users = Array.from(roomConnections.get(roomName).values());
        
        socket.emit('request_room_update_response', {
          count: onlineCount,
          room: roomName,
          project_id: project_id,
          users: users
        });
        
        console.log(`üìã Room update sent for ${roomName}: ${onlineCount} users`);
      } else {
        socket.emit('request_room_update_response', {
          count: 0,
          room: roomName,
          project_id: project_id,
          users: []
        });
      }
    } catch (error) {
      console.error('‚ùå Error sending room update:', error);
    }
  });

  // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∑–∞–ø—Ä–æ—Å–∞ –∏—Å—Ç–æ—Ä–∏–∏ —Å–æ–æ–±—â–µ–Ω–∏–π
  socket.on('get_message_history', async (roomData) => {
    try {
      const { project_id } = roomData;
      
      try {
        const response = await axios.get(`${DJANGO_URL}/api/get-messages/${project_id}/`);
        socket.emit('message_history', response.data);
        console.log(`üìö Message history sent for project ${project_id}`);
      } catch (error) {
        console.error('‚ùå Error fetching message history:', error.message);
      }
    } catch (error) {
      console.error('‚ùå Error in get_message_history:', error);
    }
  });

  // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ join_project_chat
  socket.on('join_project_chat', async (roomData) => {
    try {
      const { project_id, user_id, username } = roomData;
      const roomName = `project_${project_id}`;
      
      // –í—ã—Ö–æ–¥–∏–º –∏–∑ –ø—Ä–µ–¥—ã–¥—É—â–µ–π –∫–æ–º–Ω–∞—Ç—ã –µ—Å–ª–∏ –±—ã–ª–∞
      if (socket.roomName && socket.roomName !== roomName) {
        if (roomConnections.has(socket.roomName) && roomConnections.get(socket.roomName).has(user_id)) {
          roomConnections.get(socket.roomName).delete(user_id);
          
          const onlineCount = roomConnections.get(socket.roomName).size;
          const users = Array.from(roomConnections.get(socket.roomName).values());
          
          io.to(socket.roomName).emit('online_users_update', { 
            count: onlineCount,
            room: socket.roomName,
            project_id: socket.roomName.replace('project_', ''),
            users: users
          });
          
          if (onlineCount === 0) {
            roomConnections.delete(socket.roomName);
          }
        }
        socket.leave(socket.roomName);
      }
      
      socket.join(roomName);
      socket.roomName = roomName;
      socket.project_id = project_id;
      socket.user_id = user_id;
      
      if (!roomConnections.has(roomName)) {
        roomConnections.set(roomName, new Map());
      }
      
      // –•—Ä–∞–Ω–∏–º –ø–æ user_id –≤–º–µ—Å—Ç–æ socket.id
      roomConnections.get(roomName).set(user_id, { 
        user_id, 
        username,
        socket_id: socket.id,
        joined_at: new Date().toISOString()
      });
      
      console.log(`üë• ${username} joined project chat ${project_id}`);
      
      const onlineCount = roomConnections.get(roomName).size;
      const users = Array.from(roomConnections.get(roomName).values());
      
      io.to(roomName).emit('online_users_update', { 
        count: onlineCount,
        room: roomName,
        project_id: project_id,
        users: users
      });
      
      try {
        const response = await axios.get(`${DJANGO_URL}/api/get-messages/${project_id}/`);
        socket.emit('message_history', response.data);
      } catch (error) {
        console.error('‚ùå Error fetching message history:', error.message);
      }
      
    } catch (error) {
      console.error('‚ùå Error joining room:', error);
    }
  });

  // –û–±—Ä–∞–±–æ—Ç—á–∏–∫ send_message
  socket.on('send_message', async (messageData) => {
    try {
      const { project_id, body, user_id, username, first_name } = messageData;
      const roomName = `project_${project_id}`;
      
      console.log(`üì® Received message for saving:`, { project_id, body, user_id });
      
      const response = await axios.post(`${DJANGO_URL}/api/save-message/`, {
        project_id: project_id,
        body: body,
        author_id: user_id
      }, {
        timeout: 5000
      });

      console.log('‚úÖ Message saved in Django database:', response.data);
      
      if (response.data.status === 'success') {
        io.to(roomName).emit('receive_message', {
          id: response.data.message_id,
          body: body,
          author: user_id,
          author_name: first_name || username,
          author_username: username,
          created: response.data.created,
          project_id: project_id
        });

        console.log('‚úÖ Message sent to all clients in room:', roomName);
      } else {
        throw new Error(response.data.message || 'Unknown error from Django');
      }
      
    } catch (error) {
      console.error('‚ùå Error saving message to Django:', {
        message: error.message,
        response: error.response?.data,
        code: error.code
      });
      
      socket.emit('error', { 
        message: 'Failed to send message',
        details: error.response?.data || error.message
      });
    }
  });

}); // –ö–æ–Ω–µ—Ü io.on('connection')

